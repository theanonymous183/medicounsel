<h1>Our 3 guiding principles</h1>
 
<h3>1. Customers set the course</h3>
 
   The influence of market forces in healthcare is continually growing. This means that our customers are looking at our services more critically. They are challenging us to earn their business by offering better care, service and accessibility.<h3>2. It pays to invest in healthcare</h3>
 
